package matriculas;

public class Aluno {

	private String nome;

	private TipoAluno tipo;
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public TipoAluno getTipo() {
		return tipo;
	}

	public void setTipo(TipoAluno tipo) {
		this.tipo = tipo;
	}
	
}
